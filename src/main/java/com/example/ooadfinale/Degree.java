package com.example.ooadfinale;

/**
 * @author
 * holds degree data
 */
public class Degree {

    private String degreeID, facultyID, entryReq, feeStructure, duration, courseStructure, password, degreeName;

    public String getDegreeID() {
        return degreeID;
    }

    public void setDegreeID(String degreeID) {
        this.degreeID = degreeID;
    }

    public String getFacultyID() {
        return facultyID;
    }

    public void setFacultyID(String facultyID) {
        this.facultyID = facultyID;
    }

    public String getEntryReq() {
        return entryReq;
    }

    public void setEntryReq(String entryReq) {
        this.entryReq = entryReq;
    }

    public String getFeeStructure() {
        return feeStructure;
    }

    public void setFeeStructure(String feeStructure) {
        this.feeStructure = feeStructure;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getCourseStructure() {
        return courseStructure;
    }

    public void setCourseStructure(String courseStructure) {
        this.courseStructure = courseStructure;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getDegreeName() {
        return degreeName;
    }

    public void setDegreeName(String degreeName) {
        this.degreeName = degreeName;
    }
}
